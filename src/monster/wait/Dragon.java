package src.monster.wait;

public class Dragon {
    private int life;
    private int attack;

    public Dragon() {
        this.life = 15;
        this.attack = 4;
    }

    public int getLife() {
        return life;
    }

    public void setLife(int life) {
        this.life = life;
    }

    public int getAttack() {
        return attack;
    }

    public void setAttack(int attack) {
        this.attack = attack;
    }
}
