package src.monster.wait;

public class Sorcerer {
    private int life;
    private int attack;

    public Sorcerer() {
        this.life = 9;
        this.attack = 2;
    }

    public int getLife() {
        return life;
    }

    public void setLife(int life) {
        this.life = life;
    }

    public int getAttack() {
        return attack;
    }

    public void setAttack(int attack) {
        this.attack = attack;
    }
}
